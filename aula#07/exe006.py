nu1 = input('Digite um número: ')
nu2 = input('Digite outro número: ')
  
if nu1 == nu2:
     print(f'O {nu1} é igual de {nu2}!')
else:
    print(f'O {nu1} é diferente de {nu2}!')   

if nu1 > nu2: 
    print(f'O {nu1} é maior que {nu2}!')
if nu1 < nu2:
    print(f'O {nu1} é menor que {nu2}!')
